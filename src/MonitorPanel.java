    public void setUpdateTimeField(long aValue) {
        Date d = new Date(aValue);
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        updateTimeField.setText(df.format(d));
    }
    
