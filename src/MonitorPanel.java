    public void setBSScaleMode(int mode) {
        bsScaleField.setSelectedIndex(mode);
    }

    public double getBSScale() {
        double result = 0.1;
        switch (bsScaleField.getSelectedIndex()) {
            case 0:
                result = 0.1; // 100 [pC/count]
                break;
            case 1:
                result = 0.001; //1 [pC/count]
                break;
            default:
                break;
        }
        return result;
    }

